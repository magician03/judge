    <h1 class="panel-title">Palindromic SubString</h1>
  </div>
  <div class="panel-body" style="text-align: left">
    You have a string of lowercase letters.</br>
Now you have to find the maximum length
substring in any of the permutation of the original
string, with the condition that this substring should be
a palindrome.</br>
Output the lexicographically smallest palindromic
substring of maximum length.</br></br>

<b>Input</b> :</br>
First line will contain an integer 'T' (number of test
cases ).</br>
For each test case there is an integer 'N' (Length of the
string).</br>
Next line contains a string of lowercase letters of
length 'N'</br></br>
<b>Output</b> :</br>
For each test case output the required answer.</br></br>
<b><i>Constraint</i></b> :</br>
T<=10000</br>
1<=N<=1000000</br>
sum of N over all test cases <=10^7</br></br>
<b>Sample input</b> :</br>
3</br>
3</br>
aba</br>
4</br>
abab</br>
5</br>
abcde</br>
<b>Sample output</b> :</br>
aba</br>
abba</br>
a</br>

  </div>

